# String is ordered characters stored in memory

selfish = 'me me me'
selfish[0]
selfish[4]

# [start:stop] this format can be used to select sub-string
selfish[1:5]

# [start:stop:stepover]
selfish[1:5:2]


selfish[1:] # start at 1 and go all the way

selfish[::1]# print all

selfish[:5] #print 0 to 4

# Get reverse of string
selfish[::-1]

selfish[::-2]
