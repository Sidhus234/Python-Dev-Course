def say_hello():
    print("Helloo")
    return

say_hello()


# Parameters: inside of brackets
# Parameters 
def say_hello(name, emoji):
    print(f"Helloo {name} {emoji}")
    return

# arguements are actual values we provide to function
say_hello("John", ";P")
# In above example, parameters are name and emoji
# arguements are "John", ";P"
say_hello("Guru", ":)")
